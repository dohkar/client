/** Нормализация для поиска (убрать дефисы/пробелы, нижний регистр) */
export function normalizeCitySearch(name: string): string {
  return name
    .toLowerCase()
    .replace(/\s+/g, " ")
    .replace(/[-‑]/g, "-")
    .trim();
}

export function filterCitiesByQuery<T extends string>(
  cities: readonly T[],
  query: string
): T[] {
  if (!query.trim()) return [...cities];
  const q = normalizeCitySearch(query);
  return cities.filter((city) => normalizeCitySearch(city).includes(q));
}
